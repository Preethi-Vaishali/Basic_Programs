package Integer_Programs;

public class Incrementation {

	public static void main(String[] args) {
		int a =10;
		System.out.println("a = "+a); //a =10
		a++;//a=a+1 = 11
		
		a+=1;// ------ a= a+1 = 12
		System.out.println("a = "+a); //a =12
		a+=2; //a = a+2
		System.out.println("a = "+a); //a = 14

	}

}
