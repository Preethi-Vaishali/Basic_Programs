package Float_Programs;

public class File1 {

	public static void main(String[] args) {
		double num1, num2;
		num1 = 10.50;
		num2 = 20; //20.00
		System.out.println(num1+num2);
		System.out.printf("%f\n", (num1+num2));
		System.out.printf("%.1f\n", (num1+num2));
		System.out.printf("%.2f\n", (num1+num2));
		System.out.printf("%.3f\n", (num1+num2));
		System.out.printf("%.4f\n", (num1+num2));
		System.out.printf("%.5f\n", (num1+num2));
		System.out.printf("%f", (num1+num2));
		
	}

}
